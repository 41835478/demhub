function easeOutBounce (x) {
  var base = -Math.cos(x * (0.5 * Math.PI)) + 1;
  var rate = Math.pow(base,1.5);
  var rateR = Math.pow(1 - x, 2);
  var progress = -Math.abs(Math.cos(rate * (2.5 * Math.PI) )) + 1;
  return (1- rateR) + (progress * rateR);
}

